import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, Data } from '@angular/router';

import { PortailCssIpresV2TestModule } from '../../../test.module';
import { DocumentUrlComponent } from 'app/entities/document-url/document-url.component';
import { DocumentUrlService } from 'app/entities/document-url/document-url.service';
import { DocumentUrl } from 'app/shared/model/document-url.model';

describe('Component Tests', () => {
  describe('DocumentUrl Management Component', () => {
    let comp: DocumentUrlComponent;
    let fixture: ComponentFixture<DocumentUrlComponent>;
    let service: DocumentUrlService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [PortailCssIpresV2TestModule],
        declarations: [DocumentUrlComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: {
                subscribe: (fn: (value: Data) => void) =>
                  fn({
                    pagingParams: {
                      predicate: 'id',
                      reverse: false,
                      page: 0
                    }
                  })
              }
            }
          }
        ]
      })
        .overrideTemplate(DocumentUrlComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(DocumentUrlComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(DocumentUrlService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new DocumentUrl(123)],
            headers
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.documentUrls && comp.documentUrls[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new DocumentUrl(123)],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.documentUrls && comp.documentUrls[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
